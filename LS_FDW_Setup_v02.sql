**                                          calendar_dim
** by: mmccanlies       01/16/2017
** Update calendar_dim to set fiscal periods
***************************************************************************************************************************/
;WITH cte_tmp(dt) AS 
(
  SELECT DATEADD(MM,-3,[date]) AS dt FROM calendar_dim
)
UPDATE calendar_dim SET 
  fiscWk =  DATEPART(ISO_WEEK, dt)
, fiscMo =  DATEPART(MM, dt)
, fiscQtr = DATEPART(QQ, dt)
, fiscYr =  DATEPART(YY, dt)
, fiscYrMoStr  = RIGHT(CONVERT(varchar,DATEPART(YY, dt)),2)+'-'+SUBSTRING(CONVERT(varchar, CONVERT(smalldatetime, dt), 6),4,3)
, fiscYrQtrStr = RIGHT(CONVERT(varchar,DATEPART(YY, dt)),2)+'-Q'+CONVERT(varchar,DATEPART(QQ, dt))
, fiscYearMo = DATEPART(YY, dt)*100+ DATEPART(MM, dt)
, fmFirstDay = DATEDIFF(MM,DATEADD(DD,-1,dt),CONVERT(datetime, dt))
, fmLastDay  = DATEDIFF(MM,CONVERT(datetime, dt),DATEADD(DD,1, dt))
, fqFirstDay = DATEDIFF(QQ,DATEADD(DD,-1,dt),CONVERT(smalldatetime, dt))
, fqLastDay  = DATEDIFF(QQ,CONVERT(smalldatetime, dt),DATEADD(DD,1, dt))
, fyFirstDay = DATEPART(YY,  dt)-DATEPART(YY,DATEADD(DD,-1,  dt))
, fyLastDay  = DATEPART(YY,DATEADD(DD,1,  dt))-DATEPART(YY,  dt)
FROM cte_tmp 
JOIN calendar_dim ON DATEADD(MM,-3,calendar_dim.[date]) = cte_tmp.dt

SELECT * FROM calendar_dim 
WHERE [date] BETWEEN '20150929' AND '20180415'
AND cmFirstDay=1
ORDER BY dateId DESC


