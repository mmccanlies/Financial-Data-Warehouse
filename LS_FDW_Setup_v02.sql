**                                          calendar_dim
** by: mmccanlies       01/16/2017
** Insert calendar periods into date_dim
***************************************************************************************************************************/
;WITH cte_tmp(dt) as
(
SELECT CAST('19991231' AS smalldatetime)
UNION ALL
SELECT dt+1
FROM cte_tmp
WHERE dt< '20230131'
)
SELECT 
  CONVERT(int,CONVERT(smalldatetime, dt)) AS dateId
, CONVERT(datetime2, dt) AS [date]
, DATEPART(DY, dt) AS calDayOfYear
, DATEPART(ISO_WEEK, dt) AS calWk
, DATEPART(MM, dt) AS calMo
, DATEPART(QQ, dt) AS calQtr
, DATEPART(YY, dt) AS calYr
, RIGHT(CONVERT(varchar,DATEPART(YY, dt)),2)+'-'+SUBSTRING(CONVERT(varchar, CONVERT(smalldatetime, dt), 6),4,3) AS calYrMoStr
, RIGHT(CONVERT(varchar,DATEPART(YY, dt)),2)+'-Q'+CONVERT(varchar,DATEPART(QQ, dt)) AS calYrQtrStr
, DATEPART(YY, dt)*100+ DATEPART(MM, dt) AS calYearMo
, DATEDIFF(MM,DATEADD(DD,-1,dt),CONVERT(smalldatetime, dt)) AS cmFirstDay
, DATEDIFF(MM,CONVERT(smalldatetime, dt),DATEADD(DD,1, dt)) AS cmLastDay
, DATEDIFF(QQ,DATEADD(DD,-1,dt),CONVERT(smalldatetime, dt)) AS cqFirstDay
, DATEDIFF(QQ,CONVERT(smalldatetime, dt),DATEADD(DD,1, dt)) AS cqLastDay
, DATEPART(YY,  dt)-DATEPART(YY,DATEADD(DD,-1,  dt))   AS cyFirstDay
, DATEPART(YY,DATEADD(DD,1,  dt))-DATEPART(YY,  dt)   AS cyLastDay

, DATEPART(ISO_WEEK, dt) AS fiscWk
, DATEPART(MM, dt) AS fiscMo
, DATEPART(QQ, dt) AS fiscQtr
, DATEPART(YY, dt) AS fiscYr
, RIGHT(CONVERT(varchar,DATEPART(YY, dt)),2)+'-'+SUBSTRING(CONVERT(varchar, CONVERT(smalldatetime, dt), 6),4,3) AS fiscYrMoStr
, RIGHT(CONVERT(varchar,DATEPART(YY, dt)),2)+'-Q'+CONVERT(varchar,DATEPART(QQ, dt)) AS fiscYrQtrStr
, DATEPART(YY, dt)*100+ DATEPART(MM, dt) AS fiscYearMo
, DATEDIFF(MM,DATEADD(DD,-1,dt),CONVERT(smalldatetime, dt)) AS fmFirstDay
, DATEDIFF(MM,CONVERT(smalldatetime, dt),DATEADD(DD,1, dt)) AS fmLastDay
, DATEDIFF(QQ,DATEADD(DD,-1,dt),CONVERT(smalldatetime, dt)) AS fqFirstDay
, DATEDIFF(QQ,CONVERT(smalldatetime, dt),DATEADD(DD,1, dt)) AS fqLastDay
, DATEPART(YY,  dt)-DATEPART(YY,DATEADD(DD,-1,  dt))   AS fyFirstDay
, DATEPART(YY,DATEADD(DD,1,  dt))-DATEPART(YY,  dt)   AS fyLastDay

INTO calendar_dim
FROM cte_tmp 
OPTION (maxrecursion 0)

